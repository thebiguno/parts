package ca.digitalcave.parts.model;

public class Family {

	private String name;
	private int partsCount;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public int getPartsCount() {
		return partsCount;
	}
	public void setPartsCount(int partsCount) {
		this.partsCount = partsCount;
	}
}
